public class arithunary {
    public static void main(String[] args) {
        int a,b;
        a = 10;
        b = a++;
        System.out.println(a);
        System.out.println(b);

        a = 10;
        b = ++a;
        System.out.println(a);
        System.out.println(b);

        a = 10;
        b = a--;
        System.out.println(a);
        System.out.println(b);

        a = 10;
        b = --a;
        System.out.println(a);
        System.out.println(b);
    }
}
